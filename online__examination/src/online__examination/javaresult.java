    public javaresult(String studentID,String username,String password) {
        initComponents();
         setResizable(false);
        setLocation(200,5);
        this.username=username;
        this.password=password;
        this.studentID=studentID;
        
      try
        {
            
            Class.forName("com.mysql.jdbc.Driver");
            String database="jdbc:mysql://localhost:3306/examination";
            Connection con =  (Connection) DriverManager.getConnection(database,"root","");
            
       
            Statement stat=con.createStatement();
            String query="Select * from result_table3 where username='"+username+"'and password='"+password+"'";
            
            ResultSet rs=stat.executeQuery(query);
            
            while(rs.next())
            {               
                 int studres=rs.getInt("java_result");
                 String s_res=Integer.toString(studres);
                 studresult=s_res;
                 jLabel7.setText(studresult);
                 jLabel5.setText(studentID);
                 jLabel6.setText(username);                 
            }
        }
            catch(Exception e)
                    {
                        System.out.println(e);
                         JOptionPane.showMessageDialog(this,e.getMessage());
                    }       

    }
